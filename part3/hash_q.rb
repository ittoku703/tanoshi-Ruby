# -----------------------------------------------------------------------
# 曜日を表す英語と日本語との対応を表すwdayを定義してください。
# 例： wday[:sunday] #=> "日曜日"
# -----------------------------------------------------------------------
wday = { 
  sunday:     "日曜日", 
  monday:     "月曜日", 
  tuesday:    "火曜日", 
  wedensday:  "水曜日", 
  thursday:   "木曜日", 
  friday:     "金曜日", 
  saturday:   "土曜日" 
}
p wday[:sunday]

